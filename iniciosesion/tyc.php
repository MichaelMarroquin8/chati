Sigma Móvil S.A.S.
<br><br>


<b style="font-size:18px;">Autorización de Tratamiento de datos personales Usuarios/Consumidores y funcionarios de Aliados Comerciales – SIGMA MÓVIL S.A.S.</b>
<br><br>
Para Sigma Móvil S.A.S., sociedad debidamente constituida bajo las normas de la República de Colombia, identificada con Nit. 900.235.458-3 (en adelante, “<b>SIGMA</b>”), proteger la privacidad de sus usuarios es muy importante. Por lo tanto, <b>SIGMA</b> da estricto cumplimiento a lo dispuesto en la Ley Estatutaria 1581 de 2012, reglamentada parcialmente por los Decretos 1377 de 2013 y Decreto 886 de 2014, normas compiladas en el Decreto Único Nacional del Sector Comercio, Industria y Turismo 1074 de 2015 y aquellas normas que lo modifiquen. SIGMA le informa que sus datos serán tratados conforme a la
<b>POLÍTICA DE TRATAMIENTO DE DATOS PERSONALES</b> que se encuentra publicada tanto en la plataforma virtual AIO y CHATI LIVE como en el siguiente link para su consulta.
<br><br>

En este caso <b>SIGMA</b> actúa como Responsable del Tratamiento de los Datos Personales, por lo que mantendrá almacenados estos datos y guardará su confidencialidad atendiendo los requisitos y medidas de seguridad exigidos por la ley.
<br><br>

El tratamiento que le dará <b>SIGMA</b> a los datos personales recolectados (nombres y apellidos, número celular, dirección, correo electrónico, número de cédula, fecha de nacimiento, género) se limitará a la recolección, almacenamiento, uso, circulación mediante el uso de servidores que pueden estar ubicados en el exterior de acceso exclusivo de <b>SIGMA</b>, de su controlante, otras subordinadas o de sus proveedores, para las finalidades descritas en esta autorización, por medio de las aplicaciones móviles, aplicaciones web, sitios web y otros productos y servicios online.  Una vez se cumplan con las finalidades del tratamiento para la cual se otorgó autorización por parte del titular, los datos personales serán suprimidos de las bases de datos de <b>SIGMA</b>.
<br><br>

Finalidades de Tratamiento: El titular de la información acepta de manera expresa, voluntaria e informada el tratamiento de sus datos personales y para que estos sean consignados, sean recopilados y almacenados en las bases de datos de <b>SIGMA</b> conforme a las finalidades que a continuación se enuncian:
<br><br>

<ul>
	<li>Ordenar, catalogar, clasificar, dividir o separar y almacenar los datos personales dentro de los sistemas y archivos de <b>SIGMA MÓVIL S.A.S.</b></li>
	<li>Creación y administración de la cuenta del usuario.</li>
	<li>Prestar el mantenimiento, desarrollo y/o control de la relación comercial entre el Titular del dato personal y <b>SIGMA MÓVIL S.A.S.</b></li>
	<li>Proveer a los usuarios de la información necesaria, a través de la página Web y aplicación, sobre los canales de atención al cliente de los Aliados Comerciales, para facilitar la interacción con ellos.</li>
	<li>Realizar procesos al interior de la empresa, con fines de desarrollo operativo y/o de administración de sistemas.</li>
	<li>Prestar los servicios de la empresa y realizar el seguimiento de acuerdo con las necesidades particulares del usuario, con el fin de brindar los servicios y productos adecuados para cubrir sus necesidades específicas.</li>
	<li>Realizar el envío de información de novedades, noticias, boletines, foros de educación, publicidad o marketing, ventas a distancia. Haciendo uso de medios tales como, correo electrónico, WhatsApp, IVR (llamadas pregrabadas), notificaciones PUSH, mensajes de texto (SMS), ofertas de productos y/o servicios encontrados en la página web y la aplicación.</li>
	<li>Llevar un registro histórico de la información, con fines de satisfacción al usuario desarrollando análisis sobre los intereses y necesidades; brindando de esta manera un mejor servicio.</li>
	<li>Realizar estrategias de mercado mediante el estudio del comportamiento del usuario frente a las ofertas y con ello mejorar en su contenido, personalizando presentación y servicio.</li>
	<li>Elaboración de prospecciones comerciales y segmentación de mercados.</li>
	<li>Realizar encuestas de satisfacción y ofrecimiento o reconocimiento de beneficios propios de nuestro programa de lealtad y servicio postventa, para calificar el servicio y la atención por medio de los canales dispuestos para ello.</li>
	<li>Adelantar las actividades necesarias para gestionar las solicitudes, quejas y reclamos de los usuarios de la empresa o tercero; y direccionarlos a las áreas responsables de emitir las respuestas correspondientes.</li>
	<li>Presentar reportes ante las autoridades de inspección, vigilancia y control, y tramitar los requerimientos realizados por entidades administrativas o judiciales.</li>
	<li>Usos administrativos, comerciales y de publicidad que se establezcan en los acuerdos suscritos con los clientes Titulares de la información.</li>
	<li>Gestión contable, económica, fiscal y administrativa de clientes.</li>
	<li>Tener acceso a centrales de riesgo para conocer los estados financieros de los clientes.</li>
	<li>Conservación de la información por los términos establecidos en la Ley, especialmente el referente a la información de los libros y papeles del comerciante que deberá ser almacenada por un período de diez (10) años, según lo dispone el artículo 28 de la Ley 962 de 2005.</li>
	<li>Transferencia o Transmisión de Datos Personales nacional o internacionalmente a empresas con las que <b>SIGMA MÓVIL S.A.S.</b> desarrolle actividades en cumplimiento de su objeto social. Asimismo, se podrá hacer Transferencia a los Aliados Comerciales de la empresa para que ejecuten actividades de marketing, publicidad y promociones asociadas con su objeto social; todo de acuerdo con las disposiciones de la normativa colombiana.</li>
	<li>Remitir información a los Encargados del Tratamiento para facilitar y mejorar la calidad del servicio de <b>SIGMA MÓVIL S.A.S.</b></li>
	<li>Reportes a centrales de riesgo por incumplimiento de las obligaciones financieras derivadas de la relación comercial.</li>
	<li>Solicitar la autorización de cobro ante las entidades definidas y autorizadas para ello.</li>
</ul>
	
<br><br>

<b>SIGMA</b> podrá transmitir o transferir a terceros los datos personales de los que es Responsable. Los datos también podrán ser transferidos a otras empresas que se encuentren bajo la misma órbita de control de <b>SIGMA</b>. Dichos terceros podrán estar ubicados en Colombia, o en otros países que, a juicio de la Superintendencia de Industria y Comercio, garanticen un nivel adecuado de protección de datos personales. Igualmente, podrán ser transferidos a, Argentina, Brasil, Chile, Costa Rica, Ecuador, Estados Unidos, México, Perú, Uruguay. Así mismo, el Titular autoriza a <b>SIGMA</b> a transferir y 	
transmitir sus datos personales a terceros aliados comerciales de <b>SIGMA</b> usuarios de las Plataformas de <b>SIGMA</b>, con el fin de hacer eficiente el contacto entre el Titular y el aliado comercial, en los términos del artículo 53 de la Ley 1480 de 2011.
<br><br>


<b>Derechos que le asisten al Titular:</b> En caso en que el Titular de información otorgue autorización a <b>SIGMA</b> para el tratamiento y procesamiento de sus datos personales, los titulares de información tendrán los siguientes derechos:(i) Conocer, actualizar y rectificar y/o suprimir sus datos personales recolectados por <b>SIGMA</b>. Este derecho se puede ejercer entre otros frente a datos parciales, inexactos, incompletos, fraccionados o que induzcan al error, (ii) Solicitar prueba de la autorización otorgada a <b>SIGMA</b>, (iii) Ser informado por <b>SIGMA</b>, previa solicitud, respecto del uso que le han dado a los datos del titular, (iv) Presentar ante la Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto en la ley, (v) Revocar en cualquier tiempo la autorización  de tratamiento de datos personales, teniendo en cuenta los límites legales para ello, (vi) Acceder en forma gratuita a los datos del titular contenidos en la base de datos de <b>SIGMA</b>.
<br><br>


<b>Forma de conocer la información del Titular y cómo ejercer los derechos: SIGMA</b> presenta a elección del Titular las siguientes formas de ejercer sus derechos de “Habeas Data”:
<br><br>

<ul>
	<li>1. <b>Atención electrónica:</b> El Titular del dato podrá realizar la solicitud a través del Centro de Ayuda disponible en la plataforma de contacto AIO y/o Chati Live y/o la web de <b>SIGMA</b>.</li>
	<li>2. <b>Atención escrita:</b> El Titular del dato deberá realizar su requerimiento formal en la dirección Calle 15 Norte # 6N-34, Cali, Colombia, acreditando de forma previa y suficiente su identidad.</li>
	<li>3. <b>SIC Facilita:</b> El Titular tiene la posibilidad de acudir al canal dispuesto por la Superintendencia de Industria y Comercio.</li>
</ul>
<br><br>


Se podrá acceder a los Datos Personales si se cumple alguna las condiciones establecidas a continuación:
<br><br>

<ul>
	<li>1.	Ser Titular de la información, acreditando la identidad en forma suficiente mediante cualquier medio que <b>SIGMA</b> disponga para ello.</li>
	<li>2.	Ser causahabiente, quien deberá acreditar tal calidad.</li>
	<li>3.	Ser el representante/apoderado del Titular de la información, quien deberá acreditar tal calidad.</li>
</ul>

